package main 

import (

	"fmt"

)

func main(){

	i := 1
	//kegunaan '=' (sama dengan) disini bukan mewakili varible, tetapi untuk mengubah nilai variable
	i = i + 1
	fmt.Println(i)
	
	//menggunakan increment ++
	k := 1
	k++
	fmt.Println(k)

	//menggunakan decrement --
	j := 1
	j--
	fmt.Println(j)

}